class SourceImage {
  String name;
  String img;
  String credit;
  String license;
  String licenseUrl;

  SourceImage(String name, String img, String credit, String license, String licenseUrl) {
    this.name = name;
    this.img = img;
    this.credit = credit;
    this.license = license;
    this.licenseUrl = licenseUrl;
  }
}